    // functions to manipulate user data.

    async registration(req, res, next) {
        console.log(FgMagenta, "registration()");
        try {
            var errors = [];
            // get the user info from the body
            var user_hash = {
                full_name: req.body.full_name,
                email: req.body.email,
                phone_number: req.body.phone_number,
                is_yes_bank_customer: req.body.is_yes_bank_customer,
                referral_code: req.body.referral_code,
                session_id: req.body.session_id
            }
            //validation            
            if (!user_hash.full_name) errors.push({ message: "Full name is required." });
            if (!user_hash.email) errors.push({ message: "Email is required." });
            if (!user_hash.phone_number) errors.push({ message: "Phone number is required." });
            if (user_hash.is_yes_bank_customer == undefined) errors.push({ message: "Is Yes Bank customer is required." });
            else if (user_hash.is_yes_bank_customer != true && user_hash.is_yes_bank_customer != "true" && user_hash.is_yes_bank_customer != false && user_hash.is_yes_bank_customer != "false") errors.push({ message: "Is Yes Bank customer is invalid." });

            if (errors.length != 0) return next({ errors });

            var query = `
            INSERT INTO `+ users_collection + ` (
                full_name,                        
                email,
                phone_number,                
                is_yes_bank_customer,  
                referral_code,
                session_id,
                created_at)
                VALUES (
                "`+ user_hash.full_name + `",
                "`+ user_hash.email + `",
                "`+ user_hash.phone_number + `",
                `+ user_hash.is_yes_bank_customer + `,
                "`+ user_hash.referral_code + `",
                "`+ user_hash.session_id + `",
                `+ yesbank_campaign_pool.escape(new Date()) + `);
            `;
            await services.query(query);
            res.status(200).send({ status: true, success: "User registered successfully." });
        } catch (err) {
            console.log(FgRed, "registration() | " + err.toString());
            next({ errors: [{ message: err.toString() }] })
        }
    },

